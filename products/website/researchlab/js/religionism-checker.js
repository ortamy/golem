  // Экранирование спецсимволов в RegExp
  function escapeRegex(str) {
    return str.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
  }

  function renderDict() {
    const el = document.getElementById('rc-dict');
    if (!el) return;

    var table = document.createElement('table');
    table.className = 'lab-table';
    var thead = document.createElement('thead');
    var headerRow = document.createElement('tr');
    ['Подмена', 'Оригинал', 'Пояснение'].forEach(function(text) {
      var th = document.createElement('th');
      th.textContent = text;
      headerRow.appendChild(th);
    });
    thead.appendChild(headerRow);
    table.appendChild(thead);
    var tbody = document.createElement('tbody');

    DICT.forEach(function(entry) {
      var row = document.createElement('tr');
      var replacement = document.createElement('td');
      var replacementText = document.createElement('span');
      replacementText.className = 'highlight-red';
      replacementText.textContent = entry[0];
      replacement.appendChild(replacementText);

      var original = document.createElement('td');
      var originalText = document.createElement('strong');
      originalText.textContent = entry[1];
      original.appendChild(originalText);

      var note = document.createElement('td');
      note.className = 'text-small';
      note.textContent = entry[2];

      row.appendChild(replacement);
      row.appendChild(original);
      row.appendChild(note);
      tbody.appendChild(row);
    });

    table.appendChild(tbody);
    el.replaceChildren(table);
  }

  function appendHighlightedText(container, text, entries) {
    var position = 0;
    var found = 0;

    while (position < text.length) {
      var best = null;
      entries.forEach(function(entry) {
        var match = text.slice(position).match(new RegExp(escapeRegex(entry[0]), 'i'));
        if (!match) return;
        var index = position + match.index;
        if (!best || index < best.index || (index === best.index && entry[0].length > best.entry[0].length)) {
          best = { entry: entry, index: index, value: match[0] };
        }
      });

      if (!best) {
        container.appendChild(document.createTextNode(text.slice(position)));
        break;
      }

      if (best.index > position) {
        container.appendChild(document.createTextNode(text.slice(position, best.index)));
      }

      var marked = document.createElement('span');
      marked.className = 'highlight-red';
      marked.title = best.entry[2];
      marked.textContent = best.value;
      var arrow = document.createTextNode(' → ');
      var original = document.createElement('span');
      original.className = 'highlight-green';
      original.textContent = best.entry[1];
      container.appendChild(marked);
      container.appendChild(arrow);
      container.appendChild(original);
      found++;
      position = best.index + best.value.length;
    }

    return found;
  }

  function check() {
    const input = document.getElementById('rc-input');
    const result = document.getElementById('rc-result');
    const body = document.getElementById('rc-body');

    if (!input || !input.value.trim()) {
      result.style.display = 'block';
      body.replaceChildren();
      var emptyAlert = document.createElement('div');
      emptyAlert.className = 'lab-alert lab-alert-warn';
      emptyAlert.textContent = 'Вставьте текст для проверки.';
      body.appendChild(emptyAlert);
      return;
    }

    var text = input.value;
    var sorted = DICT.slice().sort(function(a, b) { return b[0].length - a[0].length; });
    var highlighted = document.createElement('div');
    highlighted.style.marginTop = '12px';
    highlighted.style.lineHeight = '2.2';
    highlighted.style.fontSize = '17px';
    var found = appendHighlightedText(highlighted, text, sorted);

    result.style.display = 'block';
    body.replaceChildren();

    if (found === 0) {
      var cleanAlert = document.createElement('div');
      cleanAlert.className = 'lab-alert lab-alert-success';
      cleanAlert.textContent = 'Религионизмы не обнаружены. Текст чист.';
      body.appendChild(cleanAlert);
    } else {
      var warning = document.createElement('div');
      warning.className = 'lab-alert lab-alert-warn';
      warning.appendChild(document.createTextNode('Обнаружено '));
      var count = document.createElement('strong');
      count.textContent = String(found);
      warning.appendChild(count);
      warning.appendChild(document.createTextNode(' подмен.'));
      body.appendChild(warning);
      body.appendChild(highlighted);
    }
  }

  function clearInput() {
    var input = document.getElementById('rc-input');
    if (input) input.value = '';
    var result = document.getElementById('rc-result');
    if (result) result.style.display = 'none';
  }

  return {
    init: init,
    check: check,
    clear: clearInput
  };
