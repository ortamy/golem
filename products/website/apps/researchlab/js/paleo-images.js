const PaleoImages = {
  show: function(idx) {
    var l = LETTERS[idx];
    if (!l) return;
    document.querySelectorAll('.pi-card').forEach(function(c, i) { c.classList.toggle('selected', i === idx); });
    document.getElementById('pi-dpaleo').textContent = l.paleo;
    document.getElementById('pi-dname').textContent = l.name;
    document.getElementById('pi-dtranslit').textContent = 'Транслит: ' + l.translit;
    document.getElementById('pi-dmeaning').textContent = l.meaning;
    document.getElementById('pi-ddesc').textContent = l.desc;
    document.getElementById('pi-dexample').innerHTML = '<strong>Пример:</strong> ' + l.example;
    var roots = ROOT_MAP[l.name] || [];
    var rootsEl = document.getElementById('pi-droots');
    if (roots.length > 0) {
      rootsEl.innerHTML = roots.map(function(r) { return '<span class="root-chip">' + r + '</span>'; }).join(' ');
      rootsEl.style.display = 'flex';
    } else {
      rootsEl.style.display = 'none';
    }
    document.getElementById('pi-detail').classList.add('show');
    document.getElementById('pi-detail').scrollIntoView({ behavior: 'smooth', block: 'nearest' });
  }
};
