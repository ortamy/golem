  function init() {
    renderDict();
  }

  // Экранирование спецсимволов в RegExp
  function escapeRegex(str) {
    return str.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
  }

  function renderDict() {
    const el = document.getElementById('rc-dict');
    if (!el) return;

    var html = '<table class="lab-table">' +
      '<thead><tr><th>Подмена</th><th>Оригинал</th><th>Пояснение</th></tr></thead><tbody>';

    DICT.forEach(function(entry) {
      html += '<tr>' +
        '<td><span class="highlight-red">' + entry[0] + '</span></td>' +
        '<td><strong>' + entry[1] + '</strong></td>' +
        '<td class="text-small">' + entry[2] + '</td>' +
      '</tr>';
    });

    html += '</tbody></table>';
    el.innerHTML = html;
  }

  function check() {
    const input = document.getElementById('rc-input');
    const result = document.getElementById('rc-result');
    const body = document.getElementById('rc-body');

    if (!input || !input.value.trim()) {
      result.style.display = 'block';
      body.innerHTML = '<div class="lab-alert lab-alert-warn">Вставьте текст для проверки.</div>';
      return;
    }

    var text = input.value;
    var found = 0;
    var highlighted = text;

    // Сортируем от длинных к коротким
    var sorted = DICT.slice().sort(function(a, b) { return b[0].length - a[0].length; });

    sorted.forEach(function(entry) {
      var pattern = escapeRegex(entry[0]);
      var replacement = entry[1];
      var note = entry[2];

      var regex = new RegExp('(' + pattern + ')', 'gi');
      if (regex.test(highlighted)) {
        found++;
        highlighted = highlighted.replace(regex, 
          '<span class="highlight-red" title="' + note + '">$1</span> → <span class="highlight-green">' + replacement + '</span>');
      }
    });

    result.style.display = 'block';

    if (found === 0) {
      body.innerHTML = '<div class="lab-alert lab-alert-success">Религионизмы не обнаружены. Текст чист.</div>';
    } else {
      body.innerHTML = '<div class="lab-alert lab-alert-warn">Обнаружено <strong>' + found + '</strong> подмен.</div>' +
        '<div style="margin-top:12px; line-height:2.2; font-size:17px;">' + highlighted + '</div>';
    }
  }

  function clearInput() {
    var input = document.getElementById('rc-input');
    if (input) input.value = '';
    var result = document.getElementById('rc-result');
    if (result) result.style.display = 'none';
  }

  return {
    init: init,
    check: check,
    clear: clearInput
  };
