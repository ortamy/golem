def main():
    counter = 0
    for f in sorted(os.listdir(TEACHINGS_DIR)):
        if not f.endswith('.md'):
            continue
        path = os.path.join(TEACHINGS_DIR, f)
        with open(path, 'r', encoding='utf-8') as fh:
            content = fh.read()
        
        if '[Заполнить.]' not in content and 'Статус: Черновик' not in content:
            continue  # уже заполнен
        
        new_content = generate_content(f)
        with open(path, 'w', encoding='utf-8') as fh:
            fh.write(new_content)
        counter += 1
        print(f"✅ {f}")
    
    print(f"\nИтого заполнено: {counter} файлов")


if __name__ == '__main__':
    main()