def is_stub(content):
    """Check if file is a template stub that needs filling"""
    indicators = ['[иврит]', '[имя-файла]', '[НАЗВАНИЕ КНИГИ', '[НАЗВАНИЕ СОБЫТИЯ]', 
                  '[НАЗВАНИЕ УЧЕНИЯ', '[Заполнить.]', 'TODO', 'Нет данных',
                  '[кто написал]', '[Разбор названия']
    for ind in indicators:
        if ind in content:
            return True
    # Check if first line after --- contains placeholder
    if '---' in content:
        parts = content.split('---')
        for part in parts:
            if '[иврит]' in part or '[Заполнить.]' in part:
                return True
    return False

